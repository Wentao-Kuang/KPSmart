    private void populateComboBoxes(){
        ArrayList<City> cities = new Query().citylist();
        ArrayList<TransportCompany> companies = new Query().companylist();
        
        this.jComboBox1.removeAllItems();
        this.jComboBox22.removeAllItems();
        this.jComboBox23.removeAllItems();
        
        for (int i = 0; i != cities.size(); ++i){
            this.jComboBox23.addItem(cities.get(i));
            this.jComboBox22.addItem(cities.get(i));
        }
        for (int i = 0; i != companies.size(); ++i){
            this.jComboBox1.addItem(companies.get(i));
        }
    }
    
    private void getTransportPriceChanges(){
        ArrayList<UpdateTransportPriceEvent> utpe = new Query().updateTransportList();
               
        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        
         for (int i = 0; i != utpe.size(); ++i){
            UpdateTransportPriceEvent ev = utpe.get(i);
            Object[] data = new Object[8];
            data[0] = ev.getOrigin();
            data[1] = ev.getDestination();
            data[2] = ev.getCompany();
            data[3] = ev.getPriority_id();
            data[4] = ev.getCompany_cost_pergram();
            data[5] = ev.getCompany_cost_percc();
            data[6] = ev.getDuration();
            data[7] = ev.getEvent_time();
            
            model.addRow(data);
        }
        
    }
    
